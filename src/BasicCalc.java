public class BasicCalc {


    public BasicCalc() {

    }

    public int addNumbers(int numberOne, int numberTwo) {
        return numberOne + numberTwo;
    }

    public int subtractNumbers (int numberOne, int numberTwo) {
        return numberOne - numberTwo;
    }

    public int divideNumbers(int numberOne, int numberTwo) {
        return numberOne / numberTwo;
    }

    public int multiplyNumbers(int numberOne, int numberTwo) {
        return numberOne * numberTwo;
    }

    public long addNumbers(long numberOne, long numberTwo) {
        return numberOne + numberTwo;
    }

    public long subtractNumbers (long numberOne, long numberTwo) {
        return numberOne - numberTwo;
    }

    public long divideNumbers(long numberOne, long numberTwo) {
        return numberOne / numberTwo;
    }

    public long multiplyNumbers(long numberOne, long numberTwo) {
        return numberOne * numberTwo;
    }

}
